function buildMainMatter(data: ThesisData, schema: typeof TEMPLATE_SCHEMAS[string]): ASTNode[] {
  const nodes: ASTNode[] = [];

  // \mainmatter only exists in report/book class, not article
  if (schema.documentClass !== 'article') {
    nodes.push(comment('Main Matter'));
    nodes.push(command('mainmatter'));
    nodes.push(blankLine());
  } else {
    nodes.push(comment('Main Matter'));
  }

  for (const chapter of data.chapters) {
    nodes.push(...buildChapter(chapter, data.type, schema));
  }

  return nodes;
}

function buildChapter(chapter: ThesisChapter, type: ThesisType, schema: typeof TEMPLATE_SCHEMAS[string]): ASTNode[] {
  const nodes: ASTNode[] = [];
  const chCmd = schema.chapterCommand; // \chapter or \section

  const body = (chapter.content || '').trim();

  // FIX(ZONE-3C): If body is empty and chapter is optional, skip entirely.
  // If body is empty and chapter is required, add a TODO placeholder comment.
  // This prevents producing \chapter{} with no content.
  // Core chapters = first N positions matching template bodyStructure length.
  // Optional = any chapters beyond the template's bodyStructure count.
  if (!body) {
    const coreCount = schema.bodyStructure.length;
    const isCoreChapter = chapter.number <= coreCount;
    const hasNoSubSections = chapter.subSections.length === 0;

    // Skip entirely if optional AND no subsections either
    if (!isCoreChapter && hasNoSubSections) {
      return []; // filtered out — optional empty chapter suppressed
    }

    // Required empty chapter — include with TODO comment
    nodes.push(comment(`${chCmd === '\\chapter' ? 'Chapter' : 'Section'} ${chapter.number}: ${chapter.title}`));
    nodes.push(command(chCmd.replace('\\', ''), [esc(chapter.title)]));
    nodes.push(blankLine());
    nodes.push(comment(`TODO: Add content for "${chapter.title}"`));
    nodes.push(blankLine());
  } else {
    nodes.push(comment(`${chCmd === '\\chapter' ? 'Chapter' : 'Section'} ${chapter.number}: ${chapter.title}`));
    nodes.push(command(chCmd.replace('\\', ''), [esc(chapter.title)]));
    nodes.push(blankLine());

    // Chapter content
    nodes.push(...contentToNodes(chapter.content));
    nodes.push(blankLine());
  }

  // Subsections
  const secCmd = type === 'report' ? 'subsection' : 'section';
  for (const sub of chapter.subSections) {
    if (sub.title?.trim()) {
      nodes.push(command(secCmd, [esc(sub.title)]));
      nodes.push(blankLine());
      if (sub.content && sub.content.trim()) {
        nodes.push(...contentToNodes(sub.content));
        nodes.push(blankLine());
      }
    }
  }

  return nodes;
}

