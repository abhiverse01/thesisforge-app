function buildTitlePage(data: ThesisData, schema: typeof TEMPLATE_SCHEMAS[string]): ASTNode[] {
  const nodes: ASTNode[] = [];
  const { metadata } = data;

  nodes.push(comment('Title Page'));
  nodes.push(blankLine());

  const titlePageChildren: ASTNode[] = [];

  titlePageChildren.push(command('centering'));
  titlePageChildren.push(blankLine());

  if (metadata.university) {
    titlePageChildren.push(command('scshape'));
    titlePageChildren.push(command('Large'));
    titlePageChildren.push(text(esc(metadata.university)));
    titlePageChildren.push(command('par'));
    titlePageChildren.push(blankLine());
  }
  if (metadata.faculty) {
    titlePageChildren.push(command('scshape'));
    titlePageChildren.push(command('large'));
    titlePageChildren.push(text(esc(metadata.faculty)));
    titlePageChildren.push(command('par'));
    titlePageChildren.push(blankLine());
  }
  if (metadata.department) {
    titlePageChildren.push(command('large'));
    titlePageChildren.push(text(esc(metadata.department)));
    titlePageChildren.push(command('par'));
    titlePageChildren.push(blankLine());
  }

  titlePageChildren.push(command('vspace', ['1.5cm']));
  titlePageChildren.push(blankLine());

  const typeLabels: Record<ThesisType, string> = {
    bachelor: "Bachelor's Thesis",
    master: "Master's Thesis",
    phd: 'Doctoral Dissertation',
    report: 'Research Report',
  };
  titlePageChildren.push(command('Large'));
  titlePageChildren.push(command('bfseries'));
  titlePageChildren.push(text(esc(typeLabels[data.type])));
  titlePageChildren.push(command('par'));
  titlePageChildren.push(blankLine());

  titlePageChildren.push(command('vspace', ['1cm']));
  titlePageChildren.push(blankLine());

  titlePageChildren.push(command('LARGE'));
  titlePageChildren.push(command('bfseries'));
  titlePageChildren.push(text(esc(metadata.title)));
  titlePageChildren.push(command('par'));
  titlePageChildren.push(blankLine());

  if (metadata.subtitle) {
    titlePageChildren.push(command('large'));
    titlePageChildren.push(command('itshape'));
    titlePageChildren.push(text(esc(metadata.subtitle)));
    titlePageChildren.push(command('par'));
    titlePageChildren.push(blankLine());
  }

  titlePageChildren.push(command('vspace', ['2cm']));
  titlePageChildren.push(blankLine());

  titlePageChildren.push(command('large'));
  titlePageChildren.push(text('Submitted by:'));
  titlePageChildren.push(command('par'));
  titlePageChildren.push(command('vspace', ['0.3cm']));
  titlePageChildren.push(command('Large'));
  titlePageChildren.push(command('bfseries'));
  titlePageChildren.push(text(esc(metadata.author)));
  titlePageChildren.push(command('par'));
  titlePageChildren.push(blankLine());

  if (metadata.authorId) {
    titlePageChildren.push(command('large'));
    titlePageChildren.push(text('Student ID: ' + esc(metadata.authorId)));
    titlePageChildren.push(command('par'));
    titlePageChildren.push(blankLine());
  }

  titlePageChildren.push(command('vspace', ['1.5cm']));
  titlePageChildren.push(blankLine());

  if (metadata.supervisor) {
    titlePageChildren.push(command('large'));
    titlePageChildren.push(text('Supervisor:'));
    titlePageChildren.push(command('par'));
    titlePageChildren.push(command('large'));
    titlePageChildren.push(text(esc(metadata.supervisorTitle || 'Prof.') + ' ' + esc(metadata.supervisor)));
    titlePageChildren.push(command('par'));
    titlePageChildren.push(blankLine());
  }
  if (metadata.coSupervisor) {
    titlePageChildren.push(command('large'));
    titlePageChildren.push(text('Co-Supervisor:'));
    titlePageChildren.push(command('par'));
    titlePageChildren.push(command('large'));
    titlePageChildren.push(text(esc(metadata.coSupervisorTitle || 'Dr.') + ' ' + esc(metadata.coSupervisor)));
    titlePageChildren.push(command('par'));
    titlePageChildren.push(blankLine());
  }

  titlePageChildren.push(command('vspace', ['1.5cm']));
  titlePageChildren.push(blankLine());

  if (metadata.location && metadata.submissionDate) {
    const dateObj = new Date(metadata.submissionDate);
    const formattedDate = dateObj.toLocaleDateString('en-US', { month: 'long', year: 'numeric' });
    titlePageChildren.push(command('large'));
    titlePageChildren.push(text(esc(metadata.location) + ', ' + esc(formattedDate)));
    titlePageChildren.push(command('par'));
    titlePageChildren.push(blankLine());
  }

  nodes.push(environment('titlepage', titlePageChildren));
  nodes.push(blankLine());

  return nodes;
}

