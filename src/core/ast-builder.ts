/**
 * Build a complete LaTeX AST from ThesisData.
 * This is the main entry point for the AST pipeline.
 */
export function buildAST(data: ThesisData): DocumentNode {
  const schema = TEMPLATE_SCHEMAS[data.type];
  if (!schema) {
    throw new Error(`Unknown template type: ${data.type}`);
  }

  const children: ASTNode[] = [];

  // 1. Header comment
  children.push(buildHeaderComment(data));

  // 2. Preamble (documentclass + packages + hypersetup + commands)
  children.push(...buildPreamble(data, schema));

  // 3. \begin{document}
  children.push(command('begin', ['document']));
  children.push(blankLine());

  // 4. Title page
  children.push(...buildTitlePage(data, schema));

  // 5. Front matter
  children.push(...buildFrontMatter(data, schema));

  // 6. Main matter (chapters)
  children.push(...buildMainMatter(data, schema));

  // 7. Appendices
  if (data.appendices.length > 0) {
    children.push(...buildAppendices(data, schema));
  }

  // 8. Bibliography
  children.push(...buildBibliography(data, schema));

  // 9. \end{document}
  children.push(command('end', ['document']));

  return document(children);
}

/**
 * Generate LaTeX string from ThesisData using the AST pipeline.
 */
export function generateLatexFromAST(data: ThesisData): string {
  const ast = buildAST(data);
  return serializeDocument(ast);
}

// ============================================================
// Header Comment
// ============================================================

function buildHeaderComment(data: ThesisData): CommentNode {
  const title = data.metadata.title || 'Untitled Thesis';
  const date = new Date().toISOString().split('T')[0];
  return comment(
    `============================================================\n` +
    `${title}\n` +
    `Generated by ThesisForge\n` +
    `Date: ${date}\n` +
    `Template: ${data.type}\n` +
    `============================================================`
  );
}

// ============================================================
// Preamble Builder
// ============================================================

function buildPreamble(data: ThesisData, schema: typeof TEMPLATE_SCHEMAS[string]): ASTNode[] {
  const nodes: ASTNode[] = [];

  // Document class
  const sideOption = data.type === 'phd' ? 'twoside' : 'oneside';
  const classOptions = [...schema.classOptions];
  if (!classOptions.includes(sideOption)) {
    classOptions.push(sideOption);
  }
  // Use user's format options — safely find and replace indices
  const fontSizeIdx = classOptions.findIndex((o) => ['10pt', '11pt', '12pt'].includes(o));
  if (fontSizeIdx >= 0 && !classOptions.includes(data.options.fontSize)) {
    classOptions[fontSizeIdx] = data.options.fontSize;
  }
  const paperSizeIdx = classOptions.findIndex((o) => ['a4paper', 'letterpaper'].includes(o));
  if (paperSizeIdx >= 0 && !classOptions.includes(data.options.paperSize)) {
    classOptions[paperSizeIdx] = data.options.paperSize;
  }

  nodes.push(docClass(schema.documentClass, classOptions));
  nodes.push(blankLine());

  // Packages — ordered per spec: inputenc/fontenc first, hyperref LAST
  const packages = buildPackageList(data, schema);
  for (const pkg of packages) {
    nodes.push(usepackage(pkg.packageName, pkg.options));
  }
  nodes.push(blankLine());

  // Page geometry — use \geometry{margin=...} for proper margin control
  const marginMap: Record<string, string> = { normal: '1in', narrow: '0.75in', wide: '1.25in' };
  nodes.push(command('geometry', [`margin=${marginMap[data.options.marginSize]}`]));
  nodes.push(blankLine());

  // Line spacing
  if (data.options.lineSpacing !== 'single') {
    if (data.options.lineSpacing === 'onehalf') {
      nodes.push(command('onehalfspacing'));
    } else {
      nodes.push(command('doublespacing'));
    }
    nodes.push(blankLine());
  }

  // Counter settings
  if (data.options.figureNumbering === 'per-chapter') {
    nodes.push(command('counterwithin', ['figure', 'chapter']));
  }
  if (data.options.tableNumbering === 'per-chapter') {
    nodes.push(command('counterwithin', ['table', 'chapter']));
  }
  nodes.push(blankLine());

  // Code listings (if enabled)
  if (data.options.includeListings) {
    nodes.push(command('lstset', undefined, undefined));
    // We output listings config as a comment block since the AST doesn't handle complex lstset
    nodes.push(comment(
      'Listings configuration\n' +
      '  \\lstset{\n' +
      '    basicstyle=\\ttfamily\\small,\n' +
      '    breaklines=true,\n' +
      '    frame=single,\n' +
      '    numbers=left,\n' +
      '    numberstyle=\\tiny\\color{gray},\n' +
      '    keywordstyle=\\color{blue},\n' +
      '    commentstyle=\\color{green!50!black},\n' +
      '    stringstyle=\\color{red},\n' +
      '    tabsize=2,\n' +
      '    showstringspaces=false,\n' +
      '  }'
    ));
    nodes.push(blankLine());
  }

  // Hyperref setup — MUST be after all usepackage calls
  const hyperEntries = [
    `pdftitle    = {${esc(data.metadata.title)}}`,
    `pdfauthor   = {${esc(data.metadata.author)}}`,
    `pdfsubject  = {${esc(data.metadata.subtitle || data.type + ' thesis')}}`,
    `pdfkeywords = {${esc(data.keywords.join(', '))}}`,
    `colorlinks  = true`,
    `linkcolor   = blue`,
    `citecolor   = blue`,
    `urlcolor    = blue`,
  ];
  nodes.push(text(`\\hypersetup{\n  ${hyperEntries.join(',\n  ')}\n}`, true));
  nodes.push(blankLine());

  // ToC depth
  nodes.push(command('setcounter', ['tocdepth', String(data.options.tocDepth)]));
  nodes.push(command('setcounter', ['secnumdepth', String(data.options.tocDepth)]));
  nodes.push(blankLine());

  // Custom commands
  nodes.push(comment('Custom Commands'));
  nodes.push(macroDef('\\latex', '\\LaTeX\\xspace'));
  nodes.push(macroDef('\\tex', '\\TeX\\xspace'));
  nodes.push(blankLine());

  // Theorem environments (only for report class)
  if (schema.documentClass === 'report') {
    nodes.push(comment('Theorem Environments'));
    nodes.push(command('newtheorem', ['theorem', 'Theorem', 'chapter']));
    nodes.push(command('newtheorem', ['lemma', 'Lemma', 'theorem']));
    nodes.push(command('newtheorem', ['proposition', 'Proposition', 'theorem']));
    nodes.push(command('newtheorem', ['corollary', 'Corollary', 'theorem']));
    nodes.push(command('newtheorem', ['definition', 'Definition', 'chapter']));
    nodes.push(command('newtheorem', ['example', 'Example', 'chapter']));
    nodes.push(command('newtheorem', ['remark', 'Remark', 'chapter']));
    nodes.push(blankLine());
  }

  return nodes;
}

function buildPackageList(data: ThesisData, schema: typeof TEMPLATE_SCHEMAS[string]): PackageImportNode[] {
  const pkgList: PackageImportNode[] = [];

  // Collect all package names we'll add
  const pkgNames = new Set<string>();

  // From template schema
  for (const pkg of schema.requiredPackages) {
    pkgNames.add(pkg.name);
    const existing = pkgList.find(p => p.packageName === pkg.name);
    if (!existing) {
      pkgList.push(usepackage(pkg.name, pkg.options));
    }
  }

  // Ensure critical packages are present
  const criticalPackages: Array<{ name: string; options?: string[]; before?: string }> = [
    { name: 'inputenc', options: ['utf8'] },
    { name: 'fontenc', options: ['T1'] },
    { name: 'lmodern' },
    { name: 'babel', options: ['english'] },
    { name: 'geometry' },
    { name: 'graphicx' },
    { name: 'amsmath' },
    { name: 'amssymb' },
    { name: 'booktabs' },
    { name: 'array' },
    { name: 'setspace' },
    { name: 'microtype' },
    { name: 'caption', options: ['font=small', 'labelfont=bf'] },
    { name: 'xspace' },
    { name: 'natbib', options: ['round'], before: 'hyperref' },
    { name: 'hyperref', options: ['hidelinks'] },
  ];

  // Conditionals
  if (schema.documentClass === 'report') {
    criticalPackages.push({ name: 'amsthm' });
  }
  if (data.options.figureNumbering === 'per-chapter' || data.options.tableNumbering === 'per-chapter') {
    criticalPackages.push({ name: 'chngcntr' });
  }
  if (data.options.includeListings) {
    criticalPackages.push({ name: 'listings' });
  }
  if (data.type === 'phd' && data.options.includeGlossary) {
    criticalPackages.push({ name: 'nomencl' });
    criticalPackages.push({ name: 'glossaries' });
  }

  for (const pkg of criticalPackages) {
    if (!pkgNames.has(pkg.name)) {
      pkgNames.add(pkg.name);
      pkgList.push(usepackage(pkg.name, pkg.options));
    }
  }

  // Reorder: natbib BEFORE hyperref, hyperref LAST
  const hyperrefIdx = pkgList.findIndex(p => p.packageName === 'hyperref');
  if (hyperrefIdx > -1) {
    const hyperrefPkg = pkgList.splice(hyperrefIdx, 1)[0];
    pkgList.push(hyperrefPkg);
  }
  // natbib should be second-to-last (just before hyperref)
  const natbibIdx2 = pkgList.findIndex(p => p.packageName === 'natbib');
  if (natbibIdx2 > -1 && natbibIdx2 < pkgList.length - 1) {
    const natbibPkg = pkgList.splice(natbibIdx2, 1)[0];
    pkgList.splice(pkgList.length - 1, 0, natbibPkg);
  }

  return pkgList;
}

