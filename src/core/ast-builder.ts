// ThesisForge Core — LaTeX AST Builder
// Builds a DocumentNode tree from ThesisData + template schema.
// This replaces string concatenation with a proper AST pipeline.
// Pipeline: ThesisData → ASTBuilder → ASTOptimizer → Serializer → .tex
// ============================================================

