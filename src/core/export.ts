/**
 * Category mapping from contract check IDs to ChecklistItem categories.
 */
function checkIdToCategory(id: string): ChecklistItem['category'] {
  if (id.startsWith('C')) return 'compilability';
  if (id.startsWith('P')) return 'packages';
  if (id.startsWith('S')) return 'structure';
  if (id.startsWith('B')) return 'bibliography';
  if (id.startsWith('M')) return 'metadata';
  if (id.startsWith('Q') || id.startsWith('A')) return 'quality';
  return 'compilability';
}

/**
 * Build a pre-export checklist from contract checks, intelligence report,
 * and thesis data.
 */
function buildChecklist(
  tex: string,
  bib: string,
  data: ThesisData,
  intelligence: IntelligenceReport | undefined,
): ChecklistItem[] {
  const items: ChecklistItem[] = [];

  // 1. Run all contract checks and convert to checklist items
  const fullReport = getFullContractReport(tex, bib, data);
  for (const result of fullReport) {
    items.push({
      id: result.id,
      category: checkIdToCategory(result.id),
      passed: result.passed,
      message: result.message,
      severity: result.severity as ChecklistItem['severity'],
    });
  }

  // 2. Add intelligence-derived checks
  if (intelligence) {
    // Cross-reference checks
    if (intelligence.crossRefs.undefined.length > 0) {
      const undefinedLabels = intelligence.crossRefs.undefined.map(u => u.ref).join(', ');
      items.push({
        id: 'INT-CREF',
        category: 'structure',
        passed: false,
        message: `Undefined cross-references: ${undefinedLabels}`,
        severity: 'warning',
      });
    } else if (intelligence.crossRefs.totalRefs > 0) {
      items.push({
        id: 'INT-CREF',
        category: 'structure',
        passed: true,
        message: 'All cross-references resolved',
        severity: 'info',
      });
    }

    // Orphan section checks
    for (const orphan of intelligence.orphans) {
      items.push({
        id: `INT-ORPHAN-${orphan.chapterId.slice(0, 8)}`,
        category: 'quality',
        passed: false,
        message: orphan.message,
        severity: 'warning',
      });
    }

    // Citation density checks
    for (const cd of intelligence.citationDensity) {
      if (cd.status === 'no-citations') {
        items.push({
          id: `INT-CITE-${cd.chapterId.slice(0, 8)}`,
          category: 'bibliography',
          passed: false,
          message: `"${cd.chapterTitle}" has no citations (${cd.wordCount} words)`,
          severity: 'warning',
        });
      } else if (cd.status === 'over-cited') {
        items.push({
          id: `INT-CITE-${cd.chapterId.slice(0, 8)}`,
          category: 'bibliography',
          passed: false,
          message: `"${cd.chapterTitle}" may be over-cited (${cd.density} citations/100 words)`,
          severity: 'info',
        });
      }
    }

    // Abstract quality
    if (intelligence.abstractAudit) {
      const audit = intelligence.abstractAudit;
      if (audit.wordCount > 0 && audit.passCount < 3) {
        items.push({
          id: 'INT-ABS',
          category: 'quality',
          passed: false,
          message: `Abstract quality: ${audit.passCount}/5 elements present. ${audit.issues.slice(0, 2).join(' ')}`,
          severity: 'warning',
        });
      } else if (audit.wordCount > 0 && audit.passCount >= 3) {
        items.push({
          id: 'INT-ABS',
          category: 'quality',
          passed: true,
          message: `Abstract quality: ${audit.passCount}/5 elements present`,
          severity: 'info',
        });
      }
    }

    // Chapter word count checks
    for (const wc of intelligence.chapterWordCounts) {
      if (wc.status === 'empty') {
        items.push({
          id: `INT-WC-${wc.chapterId.slice(0, 8)}`,
          category: 'quality',
          passed: false,
          message: `"${wc.chapterTitle}" is empty — no content written`,
          severity: 'error',
        });
      } else if (wc.status === 'thin') {
        items.push({
          id: `INT-WC-${wc.chapterId.slice(0, 8)}`,
          category: 'quality',
          passed: false,
          message: `"${wc.chapterTitle}" is thin (${wc.wordCount} words) — consider expanding`,
          severity: 'warning',
        });
      }
    }

    // Structure balance warnings
    for (const warning of intelligence.structureBalance.imbalanceWarnings) {
      items.push({
        id: `INT-BAL-${items.length}`,
        category: 'structure',
        passed: false,
        message: warning,
        severity: 'warning',
      });
    }
  }

  // 3. Add basic content completeness checks
  if (!data.metadata.title.trim()) {
    items.push({
      id: 'META-TITLE',
      category: 'metadata',
      passed: false,
      message: 'Thesis title is empty',
      severity: 'error',
    });
  }

  if (!data.metadata.author.trim()) {
    items.push({
      id: 'META-AUTHOR',
      category: 'metadata',
      passed: false,
      message: 'Author name is empty',
      severity: 'error',
    });
  }

  if (!data.abstract.trim()) {
    items.push({
      id: 'META-ABSTRACT',
      category: 'metadata',
      passed: false,
      message: 'Abstract is empty',
      severity: 'warning',
    });
  }

  if (data.keywords.length === 0) {
    items.push({
      id: 'META-KEYWORDS',
      category: 'metadata',
      passed: false,
      message: 'No keywords defined',
      severity: 'info',
    });
  }

  const totalWords = data.chapters.reduce((sum, ch) => sum + countWords(ch.content || ''), 0);
  if (totalWords < 100) {
    items.push({
      id: 'QUAL-LENGTH',
      category: 'quality',
      passed: false,
      message: `Very low word count (${totalWords} words). Most chapters appear empty.`,
      severity: 'warning',
    });
  }

  return items;
}

/**
 * Compute a health score (0-100) from checklist items.
 * Error failures are weighted heavily, warnings moderately, info lightly.
 */
function computeHealthScore(checklist: ChecklistItem[]): number {
  if (checklist.length === 0) return 100;

  let score = 100;
  for (const item of checklist) {
    if (item.passed) continue;
    switch (item.severity) {
      case 'error':
        score -= 15;
        break;
      case 'warning':
        score -= 5;
        break;
      case 'info':
        score -= 1;
        break;
    }
  }

  return Math.max(0, Math.min(100, score));
}

