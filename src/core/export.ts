const BIBLATEX_STYLES: Record<CitationStyle, string> = {
  ieee: 'ieee',
  apa: 'apa',
  vancouver: 'vancouver',
  chicago: 'chicago-authordate',
  harvard: 'authoryear',
};

/**
 * Generate BibLaTeX block as an alternative to natbib.
 * Returns null if BibLaTeX mode is not applicable.
 */
export function generateBibLaTeXBlock(data: ThesisData): string | null {
  const style = BIBLATEX_STYLES[data.options.citationStyle] || 'authoryear';

  const lines: string[] = [];
  lines.push('% BibLaTeX configuration (alternative to natbib)');
  lines.push(`\\usepackage[backend=biber,style=${style}]{biblatex}`);
  lines.push(`\\addbibresource{references.bib}`);
  lines.push('');

  // Add some useful BibLaTeX settings
  lines.push('% BibLaTeX fine-tuning');
  lines.push('\\DeclareFieldFormat{labeltitle}{\\textit{#1}}');
  lines.push('\\DeclareFieldFormat{title}{\\textit{#1}}');
  lines.push('\\DeclareFieldFormat[article]{pages}{#1}');
  lines.push('\\DeclareFieldFormat[inbook]{pages}{#1}');

  if (data.options.citationStyle === 'ieee') {
    lines.push('');
    lines.push('% IEEE-style numeric citations');
    lines.push('\\DeclareSourcemap{');
    lines.push('  \\maps[datatype=bibtex]{');
    lines.push('    \\map{');
    lines.push('      \\pertype{inproceedings}');
    lines.push('      \\step[fieldset=booktitle, fieldvalue={\\bibstring{inproceedings}}]');
    lines.push('    }');
    lines.push('  }');
    lines.push('}');
  }

  return lines.join('\n');
}

