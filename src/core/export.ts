/**
 * Estimate the storage usage (in KB) of the current thesis draft.
 * Includes estimated sizes for .tex, .bib, figures placeholder, README, and COMPILE.md.
 */
export function estimateStorageUsageKB(data: ThesisData): number {
  let totalBytes = 0;

  // Estimate .tex file size: ~2 bytes per character (UTF-8), plus template overhead
  const metadataValues = Object.values(data.metadata);
  const metadataChars = metadataValues.reduce((sum: number, val) => sum + String(val).length, 0);
  const abstractChars = (data.abstract || '').length;
  const keywordsChars = data.keywords.reduce((sum, kw) => sum + kw.length, 0);
  const chaptersChars = data.chapters.reduce((sum, ch) => {
    const contentLen = (ch.content || '').length;
    const subSectionsLen = ch.subSections.reduce((s, ss) => s + (ss.content || '').length + ss.title.length, 0);
    return sum + ch.title.length + contentLen + subSectionsLen;
  }, 0);
  const appendicesChars = data.appendices.reduce((sum, ap) => sum + ap.title.length + (ap.content || '').length, 0);

  // LaTeX template overhead (~3KB for preamble + structure)
  const templateOverhead = 3000;
  const contentBytes = (metadataChars + abstractChars + keywordsChars + chaptersChars + appendicesChars) * 2;
  totalBytes += templateOverhead + contentBytes;

  // Estimate .bib file size
  const bibOverheadPerRef = 300; // Average bytes per BibTeX entry
  totalBytes += data.references.length * bibOverheadPerRef;

  // README.md
  totalBytes += 1500;

  // COMPILE.md
  totalBytes += 8000;

  // Figures placeholder
  totalBytes += 500;

  // Convert to KB with 1 decimal
  return Math.round((totalBytes / 1024) * 10) / 10;
}

